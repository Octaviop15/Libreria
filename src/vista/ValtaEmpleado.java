    private void campoDniKeyTyped(java.awt.event.KeyEvent evt) {                                
         char c = evt.getKeyChar();
         int cant = campoDni.getText().length();
        
        if(!Character.isDigit(c)){
            getToolkit().beep();
            evt.consume();
        }
        
        if(cant>=8){
            getToolkit().beep();
            evt.consume();
        }
    }

   
